package Java_OOP_June_2024._01_Working_with_Abstraction._01_Lab._02_Point_In_Rectangle;

public class Point {

    private final int x;
    private final int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
